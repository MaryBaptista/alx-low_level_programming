/**
 * add - sums two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: integer
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - suntracts two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: integer
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - multiply two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: integer
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 *div - divide two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: integer
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - remainder of two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: integer
 */
int mod(int a, int b)
{
	return (a % b);
}
